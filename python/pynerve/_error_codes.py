"""Error categories and numeric codes shared with the C++ layer."""

from __future__ import annotations

from enum import IntEnum


class ErrorCategory(IntEnum):
    SUCCESS = 0
    IO_INFRA = 1
    GPU_COMPUTE = 2
    NUMERICAL = 3
    DETERMINISM = 4
    CAPACITY = 5
    ALGORITHMIC = 6
    OPERATIONAL = 7
    PH4_RESEARCH = 8
    PH5_PH6_HIGHDIM = 9
    # Category 10 reserved for future use
    NUMA_AFFINITY = 11
    PRECISION = 12
    UNKNOWN_CATEGORY = 255


class ErrorSeverity(IntEnum):
    INFO = 0
    WARNING = 1
    ERROR = 2
    CRITICAL = 3


